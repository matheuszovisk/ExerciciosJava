package curso_program;
import java.util.Scanner;

public class Exercicio_3 {

	public static void main(String[] args) {
		Scanner sc = new Scanner(System.in);
		
		System.out.println("Informe um número: ");
		int n = sc.nextInt();
		
	
		for(int i=0; i<=n; i++) {
			System.out.println(i);
		}
		
		
		
		sc.close();

	}

}
